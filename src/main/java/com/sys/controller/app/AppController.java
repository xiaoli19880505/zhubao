package com.sys.controller.app;

import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.RequestMapping;

/**
 * @author hwx
 * @CopyRight (C) 江苏乳虎
 * @date 2019/1/4 0004
 * @desc
 */
@Controller
@RequestMapping("/appPath")
public class AppController {

    /**
     * 注册
     * @return
     */
    @RequestMapping("/registe")
    public String toregiste (){
        return "zfApp/login/registe";
    }

    /**
     * 登陆
     * @return
     */
    @RequestMapping("/login")
    public String toapplogin(){
        return "zfApp/login/login";
    }

    /**
     * 忘记密码
     * @return
     */
    @RequestMapping("/forgetPwd")
    public String toforgetPwd(){
        return "zfApp/login/forgetPwd";
    }

    /**
     * 修改密码
     * @return
     */
    @RequestMapping("/updatePwd")
    public String toUpdatePwd(){
        return "zfApp/updatePwd/updatePwd";
    }

    /**
     * 个人信息
     * @return
     */
    @RequestMapping("/userInfo")
    public String userInfo(){
        return "zfApp/userInfo/userInfo";
    }
    /**
     * 申请列表
     * @return
     */
    @RequestMapping("/applyList")
    public String toApplyList(){
        return "zfApp/applyList/applyList";
    }

    /**
     * 年审列表
     * @return
     */
    @RequestMapping("/audioList")
    public String toAudioList(){
        return "zfApp/audioList/audioList";
    }




}
